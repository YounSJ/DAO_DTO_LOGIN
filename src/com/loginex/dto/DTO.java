package com.loginex.dto;

public class DTO {
	
	String name, id, pw, pwconfig;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPw() {
		return pw;
	}

	public void setPw(String pw) {
		this.pw = pw;
	}

	public String getPwconfig() {
		return pwconfig;
	}

	public void setPwconfig(String pwconfig) {
		this.pwconfig = pwconfig;
	}
	
	
}
